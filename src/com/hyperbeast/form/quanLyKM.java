    void validateKhuyenMai(int choice) {
        String tenKhuyenMai;
        Date ngayBD;
        Date ngayKT;
        Date now = new Date();
        tenKhuyenMai = tenKhuyenMaiTxt.getText();
        float mucGiam;
        String maApDung;
        int donVi;
        int trangThai;
        int maKM;
        
        ArrayList<KhuyenMai> listKM = kMModel.getKhuyenMai();
        if(tenKhuyenMai.isEmpty() || tenKhuyenMai.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập tên khuyến mãi");
            return;
        }
        ngayBD = ngayBatDauTxt.getDate();
        if(ngayBD == null) {
            JOptionPane.showMessageDialog(this, "Chưa chọn ngày bắt đầu");
            return;
        }
        ngayKT = ngayKetThucTxt.getDate();
        if(ngayKT == null) {
            JOptionPane.showMessageDialog(this, "Chưa chọn ngày kết thúc");
            return;
        }
        
        if(!ngayKT.after(ngayBD)) {
            JOptionPane.showMessageDialog(this, "Ngày kết thúc phải lớn hơn ngày bắt đầu");
            return;
        } 
        if(ngayKT.after(now)) {
            trangThaiCB.setSelectedIndex(0);
        } else {
            trangThaiCB.setSelectedIndex(1);
        }
        
        
        if(vNDRB.isSelected()) {
            donVi = 0;
        } else {
            donVi = 1;
        }
        try {
            mucGiam = Float.parseFloat(mucGiamGiaTxt.getText());
            if(mucGiam <= 0 ) {
                JOptionPane.showMessageDialog(this, "Mức giảm phải lớn hơn 0");
                return;
            }
            if(donVi == 1 && mucGiam > 100) {
                JOptionPane.showMessageDialog(this,"Phần trăm giảm phải nhỏ hơn hoặc bằng 100%");
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Mức giảm phải nhập số");
            return;
        }
        maApDung = maAPDungTxt.getText();
        if(maApDung.isEmpty() || maApDung.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập mã áp dụng");
            return;
        } else if(!(maApDung.length() == 8)) {
            JOptionPane.showMessageDialog(this, "Mã áp dụng phải có đủ 8 ký tự");
            return;
        }
        if(trangThaiCB.getSelectedIndex() == 0) {
            trangThai = 0;
        } else {
            trangThai = 1;
        }
        
        if(choice == 1) {
            java.sql.Date  ngayBatDau = new java.sql.Date(ngayBD.getTime()); 
            java.sql.Date  ngayKetThuc = new java.sql.Date(ngayKT.getTime()); 
            for (KhuyenMai km : listKM) {
                if(km.getTenKhuyenMai().equals(tenKhuyenMai) || km.getMaApDung().equals(maApDung)) {
                    JOptionPane.showMessageDialog(this, "Khuyến mãi đã tồn tại");
                    return;
                }
            }
            boolean check = kMModel.insertKhuyenMai(tenKhuyenMai, ngayBatDau, ngayKetThuc, mucGiam, maApDung, trangThai, donVi);
            if(!check) {
                JOptionPane.showMessageDialog(this, "Lưu thất bại");
                return;
            } else {
                JOptionPane.showMessageDialog(this, "Lưu thành công");
                fillKhuyenMaiTbl();
            }
        } else {
            java.sql.Date  ngayBatDau = new java.sql.Date(ngayBD.getTime()); 
            java.sql.Date  ngayKetThuc = new java.sql.Date(ngayKT.getTime()); 
            int selectedRow = khuyenMaiTbl.getSelectedRow();
            maKM = (int) khuyenMaiTbl.getValueAt(selectedRow, 0);
            boolean check = kMModel.updateKhuyenMai(maKM, tenKhuyenMai, ngayBatDau, ngayKetThuc, mucGiam, maApDung, trangThai, donVi);
            if(!check) {
                JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
                return;
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                fillKhuyenMaiTbl();
            }
        }
    }
    
    void getDataRow() {
        int selectedRow = khuyenMaiTbl.getSelectedRow();
        if(selectedRow < 0) {
            return;
        }
        titelLBl.setText("Thông tin khuyến mãi - " + khuyenMaiTbl.getValueAt(selectedRow, 0));
        Date ngayBD = (Date) khuyenMaiTbl.getValueAt(selectedRow, 2);
        Date ngayKT = (Date) khuyenMaiTbl.getValueAt(selectedRow, 3);
        tenKhuyenMaiTxt.setText("" +khuyenMaiTbl.getValueAt(selectedRow, 1));
        ngayBatDauTxt.setDate(ngayBD);
        ngayKetThucTxt.setDate(ngayKT);
        mucGiamGiaTxt.setText("" +khuyenMaiTbl.getValueAt(selectedRow, 4));
        if(khuyenMaiTbl.getValueAt(selectedRow, 5).equals("VNĐ")) {
            vNDRB.setSelected(true);
        } else {
            phanTramRB.setSelected(true);
        }
        maAPDungTxt.setText("" +khuyenMaiTbl.getValueAt(selectedRow, 6));
        trangThaiCB.setSelectedItem(khuyenMaiTbl.getValueAt(selectedRow, 7));
    }
    
    void fillKhuyenMaiTbl() {
        ArrayList<KhuyenMai> listKM = new ArrayList<>();
        listKM = kMModel.getKhuyenMai();
        DefaultTableModel model = (DefaultTableModel) khuyenMaiTbl.getModel();
        model.setRowCount(0);
        for (KhuyenMai kM : listKM) {
            Object[] data = {
                kM.getMaKH(), kM.getTenKhuyenMai(), kM.getNgayBatDau(), kM.getNgayKetThuc(), kM.getMucGiam(),
                kM.getDonVi(), kM.getMaApDung(), kM.getTrangThai()
            };
            model.addRow(data);
        }
    }
    
