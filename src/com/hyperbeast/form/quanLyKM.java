    public boolean validateKhuyenMaiTest(int choice, int maKM,String tenKhuyenMai, Date ngayBD, Date ngayKT, String maApDung, float mucGiam, int donVi, int trangThai) {
        Date now = new Date();
        
        ArrayList<KhuyenMai> listKM = kMModel.getKhuyenMai();
        if(tenKhuyenMai.isEmpty() || tenKhuyenMai.trim().isEmpty()) {
            throw new NullPointerException("tên khuyến mại rỗng");
        }
        ngayBD = ngayBatDauTxt.getDate();
        if(ngayBD == null) {
            throw new NullPointerException("nhập ngày bắt đầu rỗng");
        }
        ngayKT = ngayKetThucTxt.getDate();
        if(ngayKT == null) {
            throw new NullPointerException("ngày kết thúc rỗng");
        }
        if(trangThai != 1 || trangThai != 2) {
            throw new IllegalArgumentException("Trạng thái không hợp lệ");
        }
        
        if(!ngayKT.after(ngayBD)) {
            throw new IllegalArgumentException("Ngày kết thúc nhỏ hơn ngày bắt đầu");
        } 
        try {
            if(mucGiam <= 0 ) {
                throw new IllegalArgumentException("Mức giảm nhỏ hơn 0");
            }
            if(donVi == 1 && mucGiam > 100) {
                throw new IllegalArgumentException("Mức giảm % lớn hơn 100");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Mức giảm phải nhập số");
            return false;
        }
        if(maApDung.isEmpty() || maApDung.trim().isEmpty()) {
            throw new NullPointerException("mã áp dụng rỗng");
        } else if(!(maApDung.length() == 8)) {
            throw new IllegalArgumentException("Mã áp dụng không đủ 8 ký tự");
        }
        
        if(choice == 1) {
            java.sql.Date  ngayBatDau = new java.sql.Date(ngayBD.getTime()); 
            java.sql.Date  ngayKetThuc = new java.sql.Date(ngayKT.getTime()); 
            for (KhuyenMai km : listKM) {
                if(km.getTenKhuyenMai().equals(tenKhuyenMai) || km.getMaApDung().equals(maApDung)) {
                    throw new IllegalArgumentException("Khuyến mại đã tồn tại");
                }
            }
            boolean check = kMModel.insertKhuyenMai(tenKhuyenMai, ngayBatDau, ngayKetThuc, mucGiam, maApDung, trangThai, donVi);
            if(!check) {
                return false;
            } else {
                return true;
            }
        } else if(choice == 2){
            java.sql.Date  ngayBatDau = new java.sql.Date(ngayBD.getTime()); 
            java.sql.Date  ngayKetThuc = new java.sql.Date(ngayKT.getTime()); 
            for (KhuyenMai km : listKM) {
                if(!(maKM == km.getMaKH())) {
                    throw new IllegalArgumentException("Khuyến mại không tồn tại");
                }
            }
            boolean check = kMModel.updateKhuyenMai(maKM, tenKhuyenMai, ngayBatDau, ngayKetThuc, mucGiam, maApDung, trangThai, donVi);
            if(!check) {
                 return false;
            } else {
                return true;
            }
        } else {
            throw new IllegalArgumentException("sai lựa chọn");
        }
    }
